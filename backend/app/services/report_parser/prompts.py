def build_system_prompt(report_type: str) -> str:
    """Выбрать системный промпт по типу компании."""
    if report_type == "bank":
        return SYSTEM_PROMPT_BANK
    return SYSTEM_PROMPT_GENERAL


def build_user_prompt(
    *,
    ticker: str,
    expected_year: int,
    company_name: str,
    sector: Optional[str],
    pdf_text: str,
    is_scanned: bool = False,
) -> str:
    return USER_PROMPT_TEMPLATE.format(
        ticker=ticker,
        expected_year=expected_year,
        company_name=company_name,
        sector=sector or "неизвестен",
        pdf_text=pdf_text,
        scan_notice=_SCAN_NOTICE if is_scanned else "",
    )
