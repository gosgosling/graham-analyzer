"""Промпты для LLM-извлечения финансовых данных из отчётов.

Принципы:
  * Четкое разделение general/bank — разные наборы полей.
  * Явное указание: null вместо выдумывания.
  * Требование писать заметки в extraction_notes для всех неуверенных случаев.
  * Строгое указание единиц — числа остаются в ИСХОДНЫХ единицах отчёта,
    перевод в млн делается на нашей стороне (надёжнее, чем в LLM).
"""
from __future__ import annotations

from typing import Optional


SYSTEM_PROMPT_GENERAL = """\
Ты — точный финансовый аналитик. Тебе дают фрагменты из русскоязычного \
консолидированного годового отчёта по МСФО промышленной, нефтегазовой или \
ритейловой компании. Твоя задача — вернуть СТРУКТУРИРОВАННЫЙ JSON с ключевыми \
финансовыми показателями для последующего анализа по методу Бенджамина Грэма.

ПРАВИЛА:
1. Возвращай СТРОГО JSON по схеме, без комментариев и markdown.
2. Если показатель НЕ найден в тексте — ставь null, НЕ выдумывай.
3. Числа пиши в ТЕХ ЖЕ единицах, в которых они приведены в отчёте, и отдельно \
сообщай units_scale: 'units' | 'thousands' | 'millions' | 'billions'.
4. Для баланса бери цифры НА КОНЕЦ отчётного периода (обычно правая колонка \
'на 31 декабря 20XX'). НЕ путай с предыдущим годом.
5. Для ОПиУ бери цифры ЗА ОТЧЁТНЫЙ ГОД (колонка '20XX'), не за сравнительный \
предыдущий.
6. Нормализованную чистую прибыль клади в net_income. Если указана \
отдельно 'базовая'/'разводнённая' — бери базовую. Если есть строка \
'прибыль, относящаяся к акционерам ПАО/материнской компании' — бери её.
7. В net_income_reported клади ту же 'Чистая прибыль' без нормализаций \
(общая, до распределения между акционерами и неконтролирующими долями), \
если эти значения различаются. Если одинаковые — можно скопировать.
8. Поле extraction_notes ОБЯЗАТЕЛЬНО заполни: 1-5 коротких строк о том, \
какие поля были неочевидны, что нормализовал, какие значения не нашлись. \
Это увидит человек-аналитик при проверке.
9. confidence: 'high' если все ключевые поля чётко нашлись; 'medium' если \
есть спорные; 'low' если много пропусков.
10. Для промышленной компании report_type = 'general'. Поля банковского \
блока (net_interest_income, fee_commission_income, operating_expenses, \
provisions) оставляй null.
"""

SYSTEM_PROMPT_BANK = """\
Ты — точный финансовый аналитик. Тебе дают фрагменты из русскоязычного \
консолидированного годового отчёта БАНКА по МСФО. Твоя задача — вернуть \
СТРУКТУРИРОВАННЫЙ JSON с ключевыми финансовыми показателями для последующего \
анализа по адаптированному методу Грэма.

ПРАВИЛА:
1. Возвращай СТРОГО JSON по схеме, без комментариев и markdown.
2. Если показатель НЕ найден — ставь null, НЕ выдумывай.
3. Числа пиши в ТЕХ ЖЕ единицах, в которых они в отчёте, и отдельно сообщай \
units_scale: 'units' | 'thousands' | 'millions' | 'billions'.
4. Для баланса бери цифры НА КОНЕЦ отчётного периода.
5. Для ОПиУ — цифры ЗА ОТЧЁТНЫЙ ГОД.
6. report_type = 'bank'.
7. revenue = Total Operating Income (сумма: чистые процентные доходы + чистые \
комиссионные доходы + торговые доходы + прочие операц. доходы). Если в отчёте \
уже есть строка 'Итого операционные доходы' — используй её.
8. net_interest_income = чистые процентные доходы (после расходов).
9. fee_commission_income = чистые комиссионные доходы (доходы минус расходы \
от комиссий).
10. operating_expenses = операционные расходы ДО резервов (положительное число, \
без знака).
11. provisions = расходы по формированию резервов под обесценение кредитов \
(положительное число — размер резерва).
12. current_assets и current_liabilities для банков НЕ заполняются (null) — \
у банков нет понятия оборотных активов.
13. net_income = прибыль, относящаяся к акционерам банка (после \
неконтролирующих долей).
14. dividends_per_share — дивиденды на обыкновенную акцию (не префы!).
15. Заметки в extraction_notes — обязательно: какие строки сомнительны, \
как рассчитал revenue, что с неконтролирующими долями.
"""


USER_PROMPT_TEMPLATE = """\
ТИКЕР: {ticker}
ОЖИДАЕМЫЙ ГОД: {expected_year}
КОМПАНИЯ: {company_name}
СЕКТОР: {sector}

Ниже — релевантные страницы из PDF-отчёта. Страницы промаркированы \
заголовками вида '───── СТРАНИЦА N из M ─────'. Это НЕ весь отчёт, а только \
выделенные разделы: баланс, отчёт о прибылях и убытках, отчёт об изменениях \
в капитале, банковские доходы.

Внимательно прочитай, найди показатели и верни JSON по требуемой схеме.

=== НАЧАЛО ФРАГМЕНТА ===
{pdf_text}
=== КОНЕЦ ФРАГМЕНТА ===
"""


def build_system_prompt(report_type: str) -> str:
    """Выбрать системный промпт по типу компании."""
    if report_type == "bank":
        return SYSTEM_PROMPT_BANK
    return SYSTEM_PROMPT_GENERAL


def build_user_prompt(
    *,
    ticker: str,
    expected_year: int,
    company_name: str,
    sector: Optional[str],
    pdf_text: str,
) -> str:
    return USER_PROMPT_TEMPLATE.format(
        ticker=ticker,
        expected_year=expected_year,
        company_name=company_name,
        sector=sector or "неизвестен",
        pdf_text=pdf_text,
    )
